    public void setTaskData(int tarea_id){
        this.setLocationRelativeTo(null);
        results=task.findById(tarea_id);
        try{
            if (results.first()) {
                this.terminada_fecha.setText(results.getString("terminada"));
                this.txt_show_desc.setText(results.getString("completada_desc"));
                this.titulo_tarea.setText(results.getString("titulo"));
            }
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }

